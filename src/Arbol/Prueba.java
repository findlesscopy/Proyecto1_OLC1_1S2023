package Arbol;

public class Prueba {
    private String nombre;
    private String cadena;

    public Prueba(String nombre, String cadena) {
        this.nombre = nombre;
        this.cadena = cadena;
    }

    public String getNombre() {
        return nombre;
    }

    public String getCadena() {
        return cadena;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public void setCadena(String cadena) {
        this.cadena = cadena;
    }
}
